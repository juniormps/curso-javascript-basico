let num = [5, 8, 2, 9, 3]

console.log(num)

console.log(`O vetor tem ${num.length} posições.`)

console.log(`O primeiro valor do vetor é ${num[0]}`)

num.sort()  //coloca os valores do vetor em ordem crescente

console.log(`Agora com os valores do vetor em ordem crescente,  ele fica assim: ${num}`)

num.push(1)  //adiciona o valor entre parênteses na última posição do vetor

console.log(`Agora, após adicionar o valor 1 na última posição,  ele fica assim: ${num}`)